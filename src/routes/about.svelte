  <h3>YEP setzt sich mit Jugendlichen für Jugendliche ein</h3>
  <p>
    Für mehr Mitsprache und wahre Mitbestimmung in der Gesellschaft. “If it is about them, don’t do
    it without them.” Jugendliche sind Expert*innen ihrer Lebenswelt und als diese sollten sie
    respektiert werden. Für eine verantwortungsvolle Gesellschaft ist es wichtig, die Stimme der
    Jugend ernst zu nehmen, zu fordern und zu fördern. <br />
  </p>

  <h3>Sichtbarmachen von Potentialen und Talenten</h3>

  <p>
    Für YEP steht das Sichtbarmachen von Potentialen und Talenten von jungen Menschen im
    Vordergrund. Jugendliche werden dabei begleitet, sich in den gesellschaftlichen Dialog
    einzubringen, ihre eigenen Ideen und Projekte zu entwickeln und umzusetzen. Die
    Projektbegleitung findet über Coaching und Workshops statt, die sich Jugendliche ganz nach dem
    “Create your own education!” Prinzip selbst zusammenstellen können. <br />
    Hier soll in <strong>Kooperation mit Organisationen </strong>und Expert*innen verschiedener
    Bereiche ein außerschulischer Lernort geschaffen werden, der sich ganz an dem Bedarf und den
    Bedürfnissen von Jugendlichen orientiert. Besonders wichtig ist der niederschwellige Zugang, YEP
    hat sich zur Aufgabe gestellt, das Angebot an alle Jugendlichen zu adressieren, sowohl
    Schüler*innen, Lehrlinge als auch junge Erwachsene, und auch auf benachteiligte Gruppen
    besonderen Augenmerk zu legen.
  </p>

  <h3>Mit YEP als Partner zu mehr Mitbestimmung</h3>
  <p>
    Für Organisationen/Unternehmen/Institutionen stellt YEP einen wichtigen Partner für
    Demokratisierung und Mitbestimmung dar und unterstützt dabei Partizipationsprozesse aufzusetzen,
    um sie mit Jugendlichen zu verbinden und die immer wichtiger werdende Stimme der Jugend
    einzubringen. Niemand ist der Zukunft näher als Jugendliche - ihre Ideen und ihre Mitbeteiligung
    sind mit ein Schlüssel für innovative und werteorientierte Unternehmenskultur. YEP soll ein
    Katalysator für Jugendliche sein, die in der Welt etwas bewegen, mitreden und ihre Zukunft
    mitgestalten wollen - und für Unternehmen, denen die Stimme der Jugend wichtig ist.
  </p>

