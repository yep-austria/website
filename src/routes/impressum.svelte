    YEP - Stimme der Jugend<br />
    Verein für Mitbestimmung, gesellschaftliche Partizipation und Empowerment von Jugendlichen.
  </p>

  <h3>Vereinszweck</h3>
  <p>
    Die Vereinstätigkeit ist nicht auf Gewinn ausgerichtet und bezweckt ausschließlich und
    unmittelbar die Förderung von Jugendlichen im Bereich Bildung, Selbstermächtigung, Teilhabe und
    Mitbestimmung in der Gesellschaft. <br />
    Der Verein ist unabhängig, überparteilich und dessen Tätigkeit ist nicht auf Gewinn ausgerichtet.
    Das Ziel ist Jugendliche zu ermächtigen an gesellschaftlichen Prozessen teilzuhaben, strukturelle
    Voraussetzungen hierfür zu erwirken und somit zu einer höheren Breite an demokratischer Teilhabe
    in der Gesellschaft zu führen. <br />
    Der Verein fühlt sich den Werten der Allgemeinen Erklärung der Menschenrechte verpflichtet, agiert
    werteorientiert auf diesen Grundsätzen & setzt sich für die Verbreitung dieser Werte ein.
  </p>

